package com.migu.online.vo;

import java.math.BigDecimal;
import java.util.Date;

//用户
public class UserVo {

	private Long id;
	private String name;
	private String mobile;
	private String avatar;
	private String sessionId;
	private int isBuyCard; // 1:已购买 0：未购买
	private int isVip; // 1:是 0：不是
	private String vipKemu; // '1：科目一 2：科目二 3：科目三 4：科目四 5:4类组合 逗号分隔'
	private BigDecimal cardPrice;
	private String realName;
	private String idNo;
	private Date cardExpiredTime; // 过期具体日期 如果为空，说明没激活
	private Long cardExpiredDays; // 过期天数
	private Boolean cardIsExpired; // 是否过期

	public String getRealName() {
		return realName;
	}

	public void setRealName(String realName) {
		this.realName = realName;
	}

	public String getIdNo() {
		return idNo;
	}

	public void setIdNo(String idNo) {
		this.idNo = idNo;
	}

	public int getIsBuyCard() {
		return isBuyCard;
	}

	public void setIsBuyCard(int isBuyCard) {
		this.isBuyCard = isBuyCard;
	}

	public BigDecimal getCardPrice() {
		return cardPrice;
	}

	public void setCardPrice(BigDecimal cardPrice) {
		this.cardPrice = cardPrice;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getAvatar() {
		return avatar;
	}

	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}

	public String getSessionId() {
		return sessionId;
	}

	public void setSessionId(String sessionId) {
		this.sessionId = sessionId;
	}

	public int getIsVip() {
		return isVip;
	}

	public void setIsVip(int isVip) {
		this.isVip = isVip;
	}

	public String getVipKemu() {
		return vipKemu;
	}

	public void setVipKemu(String vipKemu) {
		this.vipKemu = vipKemu;
	}

	public Date getCardExpiredTime() {
		return cardExpiredTime;
	}

	public void setCardExpiredTime(Date cardExpiredTime) {
		this.cardExpiredTime = cardExpiredTime;
	}

	public Long getCardExpiredDays() {
		return cardExpiredDays;
	}

	public void setCardExpiredDays(Long cardExpiredDays) {
		this.cardExpiredDays = cardExpiredDays;
	}

	public Boolean getCardIsExpired() {
		return cardIsExpired;
	}

	public void setCardIsExpired(Boolean cardIsExpired) {
		this.cardIsExpired = cardIsExpired;
	}
	
}
