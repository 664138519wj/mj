package com.migu.online.vo;

import java.io.Serializable;

/**
 * 考试题库
 * @author fanyunlong
 *
 */
public class ExamTkVo implements Serializable{
	
	private static final long serialVersionUID = 7013877876373050465L;

	private int index = 1; // 题目序号 当前第几题 默认1
	
	private String examCode; // 题库编码

    private String question; // 问题
    
    private String answer1; // 选项1
    
    private String answer2; // 选项2

    private String answer3; // 选项3

    private String answer4; // 选项4
    
    private String corrAnswer; // 正确答案
    
    private String imageUrl; // 图片地址
    
    private Integer quesType; // 问题类型 1-判断 2-单选 3-多选
    
    private Integer quesAttr; // 试题属性 1-非看图题 2-看图题
    
    private String desc; // 解释
    
    private int language; // 语言 默认 0:中文 1：蒙语 2：哈语 3：维语
    
    private Integer kemu; // 科目
    
    private String examType; // 问题类型 A01~A07 B01~B08 
    
    private Long examId; // 用户生成试卷id 模拟习题才有
       

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public String getExamCode() {
		return examCode;
	}

	public void setExamCode(String examCode) {
		this.examCode = examCode;
	}

	public String getQuestion() {
		return question;
	}

	public void setQuestion(String question) {
		this.question = question;
	}

	public String getAnswer1() {
		return answer1;
	}

	public void setAnswer1(String answer1) {
		this.answer1 = answer1;
	}

	public String getAnswer2() {
		return answer2;
	}

	public void setAnswer2(String answer2) {
		this.answer2 = answer2;
	}

	public String getAnswer3() {
		return answer3;
	}

	public void setAnswer3(String answer3) {
		this.answer3 = answer3;
	}

	public String getAnswer4() {
		return answer4;
	}

	public void setAnswer4(String answer4) {
		this.answer4 = answer4;
	}

	public String getCorrAnswer() {
		return corrAnswer;
	}

	public void setCorrAnswer(String corrAnswer) {
		this.corrAnswer = corrAnswer;
	}

	public String getImageUrl() {
		return imageUrl;
	}

	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}

	public Integer getQuesType() {
		return quesType;
	}

	public void setQuesType(Integer quesType) {
		this.quesType = quesType;
	}

	public Integer getQuesAttr() {
		return quesAttr;
	}

	public void setQuesAttr(Integer quesAttr) {
		this.quesAttr = quesAttr;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public int getLanguage() {
		return language;
	}

	public void setLanguage(int language) {
		this.language = language;
	}

	public Integer getKemu() {
		return kemu;
	}

	public void setKemu(Integer kemu) {
		this.kemu = kemu;
	}

	public String getExamType() {
		return examType;
	}

	public void setExamType(String examType) {
		this.examType = examType;
	}

	public Long getExamId() {
		return examId;
	}

	public void setExamId(Long examId) {
		this.examId = examId;
	}
	
}
