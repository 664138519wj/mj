package com.migu.online.vo;

/**
 * 用户答题
 * @author fanyunlong
 *
 */
public class ExamTkAnsVo {
	
	private int index = 1; // 题目序号 当前第几题 默认1
	
	private String examCode; // 题库编码
    
    private int language; // 语言 默认 0：中文 1：哈语 2：蒙语 3：维语 
    
    private String answer; // 用户答案
    
    private int isCorrect; // 用户答对还是答错 0:未答题 1：正确 2：错误

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public String getExamCode() {
		return examCode;
	}

	public void setExamCode(String examCode) {
		this.examCode = examCode;
	}

	public int getLanguage() {
		return language;
	}

	public void setLanguage(int language) {
		this.language = language;
	}

	public String getAnswer() {
		return answer;
	}

	public void setAnswer(String answer) {
		this.answer = answer;
	}

	public int getIsCorrect() {
		return isCorrect;
	}

	public void setIsCorrect(int isCorrect) {
		this.isCorrect = isCorrect;
	}
       
}
