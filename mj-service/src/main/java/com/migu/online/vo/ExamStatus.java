package com.migu.online.vo;

import java.io.Serializable;

/**
 * 考试状态
 * @author fanyunlong
 *
 */
public class ExamStatus implements Serializable{

	private static final long serialVersionUID = 5998466782166823113L;
	
	private boolean isUnFinish = false;
	
	private int lastIndex = 1;
	
	private Long seconds = 0L; // 剩余秒
	
	private Integer kemu;
	
	private Integer licType;
	
	private Long examId;

	public boolean isUnFinish() {
		return isUnFinish;
	}

	public void setUnFinish(boolean isUnFinish) {
		this.isUnFinish = isUnFinish;
	}

	public int getLastIndex() {
		return lastIndex;
	}

	public void setLastIndex(int lastIndex) {
		this.lastIndex = lastIndex;
	}

	public Long getSeconds() {
		return seconds;
	}

	public void setSeconds(Long seconds) {
		this.seconds = seconds;
	}

	public Integer getKemu() {
		return kemu;
	}

	public void setKemu(Integer kemu) {
		this.kemu = kemu;
	}

	public Integer getLicType() {
		return licType;
	}

	public void setLicType(Integer licType) {
		this.licType = licType;
	}

	public Long getExamId() {
		return examId;
	}

	public void setExamId(Long examId) {
		this.examId = examId;
	}
		
}
