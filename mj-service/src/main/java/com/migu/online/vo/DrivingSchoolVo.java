package com.migu.online.vo;

import java.math.BigDecimal;
import java.util.List;

/*驾校*/
public class DrivingSchoolVo {
	
	private Integer id;
	private String name; 
	private String address;
	private String tel;
	private String imagePath; // 头像
	private List<String> shufflingImageList; // 图片轮播 逗号分隔
	private String licence;
	private BigDecimal price;
	private List<DrivingSchoolLessonVo> lessonList;
	
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getImagePath() {
		return imagePath;
	}

	public void setImagePath(String imagePath) {
		this.imagePath = imagePath;
	}
	
	public List<String> getShufflingImageList() {
		return shufflingImageList;
	}

	public void setShufflingImageList(List<String> shufflingImageList) {
		this.shufflingImageList = shufflingImageList;
	}

	public String getLicence() {
		return licence;
	}

	public void setLicence(String licence) {
		this.licence = licence;
	}

	public BigDecimal getPrice() {
		return price;
	}

	public void setPrice(BigDecimal price) {
		this.price = price;
	}

	public List<DrivingSchoolLessonVo> getLessonList() {
		return lessonList;
	}

	public void setLessonList(List<DrivingSchoolLessonVo> lessonList) {
		this.lessonList = lessonList;
	}	
		
}
