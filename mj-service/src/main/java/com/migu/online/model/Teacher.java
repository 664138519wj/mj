package com.migu.online.model;

import java.util.Date;

import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;

import org.apache.ibatis.type.Alias;

import lombok.Data;

//教练
@Alias("teacher")
@Data
public class Teacher {
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	private Long id;
	private String name;
	private String idNo;
	private String teacherNo;
	private String mobile;
	private Integer teachingAge;
	private String avator;
	private Integer schoolId;
	private String tag;
	private String introduce;
	private String teachingAddress;
	private Integer isDelete; // 0 normal  2：待审核 1：delete
	private Integer recommand;
	private Integer sort;
	private Double onlineBenefitShare;
	private Double offlineBenefitShare;
	private Date createTime;
	private Date updateTime;
	
	private String address; // 地址
	private Integer drivingAge; // 驾龄
	private String photo; // 照片
	private String coachPhoto; //
	private String idUp;
	private String idDown;
	private String schoolName;
	private String loginName;
	private Integer score; // 学员评分
		
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIdNo() {
		return idNo;
	}

	public void setIdNo(String idNo) {
		this.idNo = idNo;
	}

	public String getTeacherNo() {
		return teacherNo;
	}

	public void setTeacherNo(String teacherNo) {
		this.teacherNo = teacherNo;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public Integer getTeachingAge() {
		return teachingAge;
	}

	public void setTeachingAge(Integer teachingAge) {
		this.teachingAge = teachingAge;
	}

	public String getAvator() {
		return avator;
	}

	public void setAvator(String avator) {
		this.avator = avator;
	}

	public Integer getSchoolId() {
		return schoolId;
	}

	public void setSchoolId(Integer schoolId) {
		this.schoolId = schoolId;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

	public String getIntroduce() {
		return introduce;
	}

	public void setIntroduce(String introduce) {
		this.introduce = introduce;
	}

	public Integer getSort() {
		return sort;
	}

	public void setSort(Integer sort) {
		this.sort = sort;
	}

	public Double getOnlineBenefitShare() {
		return onlineBenefitShare;
	}

	public void setOnlineBenefitShare(Double onlineBenefitShare) {
		this.onlineBenefitShare = onlineBenefitShare;
	}

	public Double getOfflineBenefitShare() {
		return offlineBenefitShare;
	}

	public void setOfflineBenefitShare(Double offlineBenefitShare) {
		this.offlineBenefitShare = offlineBenefitShare;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}

	public Integer getIsDelete() {
		return isDelete;
	}

	public void setIsDelete(Integer isDelete) {
		this.isDelete = isDelete;
	}

	public Integer getRecommand() {
		return recommand;
	}

	public void setRecommand(Integer recommand) {
		this.recommand = recommand;
	}

	public String getTeachingAddress() {
		return teachingAddress;
	}

	public void setTeachingAddress(String teachingAddress) {
		this.teachingAddress = teachingAddress;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public Integer getDrivingAge() {
		return drivingAge;
	}

	public void setDrivingAge(Integer drivingAge) {
		this.drivingAge = drivingAge;
	}

	public String getPhoto() {
		return photo;
	}

	public void setPhoto(String photo) {
		this.photo = photo;
	}

	public String getCoachPhoto() {
		return coachPhoto;
	}

	public void setCoachPhoto(String coachPhoto) {
		this.coachPhoto = coachPhoto;
	}

	public String getIdUp() {
		return idUp;
	}

	public void setIdUp(String idUp) {
		this.idUp = idUp;
	}

	public String getIdDown() {
		return idDown;
	}

	public void setIdDown(String idDown) {
		this.idDown = idDown;
	}

	public String getSchoolName() {
		return schoolName;
	}

	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}

	public String getLoginName() {
		return loginName;
	}

	public void setLoginName(String loginName) {
		this.loginName = loginName;
	}

	public Integer getScore() {
		return score;
	}

	public void setScore(Integer score) {
		this.score = score;
	}	
	
	
	
}
